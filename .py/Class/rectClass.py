class rectangle:
    length=0;
    breadth=0;
r1=rectangle()
print ("initial Value of the attributes are\n",r1.length," ",r1.breadth)
print("area= ",r1.length*r1.breadth)
r1.length=20
r1.breadth=10
print ("Updated Value of the attributes are\n",r1.length," ",r1.breadth)
print("area= ",r1.length*r1.breadth)
